#include <bits/stdc++.h>

int main() noexcept {
    std::cout << std::fixed << std::setprecision(1) << 1.2330000f << '\n';
    std::cout << 114.514f << '\n';
    std::cout << std::oct << 114514 << '\n';
    std::cout << std::hex << 114514 << '\n';
    std::cout << std::bitset<16>(114514) << '\n';
    std::cout << std::bitset<16>(-114514) << '\n';
    std::cout << std::bitset<16>(114514U) << '\n';
    return 0;
}